//Foundry has some functions that don't quite match JS for use in eval(). Defining the short names here now.

function max(a, b){
	return Math.max(a,b);
}

function min(a, b){
	return Math.min(a,b);
}

function ceil(a){
    return Math.ceil(a);
}

function floor(a){
    return Math.floor(a);
}